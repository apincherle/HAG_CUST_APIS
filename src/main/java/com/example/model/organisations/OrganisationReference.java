package com.example.model.organisations;

public class OrganisationReference {
    private String xid;
    private String name;

    // Getters and Setters
    public String getXid() {
        return xid;
    }

    public void setXid(String xid) {
        this.xid = xid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
} 